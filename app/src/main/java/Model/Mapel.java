package Model;

public class Mapel {
    public String nama_guru;
    public String nama_mapel;
    public String lokasi;
    public String score;

    public Mapel(){

    }

    public Mapel(String nama_guru, String nama_mapel, String lokasi, String score) {
        this.nama_guru = nama_guru;
        this.nama_mapel = nama_mapel;
        this.lokasi = lokasi;
        this.score = score;
    }

}
